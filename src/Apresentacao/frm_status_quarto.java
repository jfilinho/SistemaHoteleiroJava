    public class pintar_linha extends DefaultTableCellRenderer {

        public pintar_linha() {
        }

        @Override
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            JLabel label = (JLabel) super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column); //To change body of generated methods, choose Tools | Templates.

            Color backgroud = Color.GREEN;

            Object objeto = TB_lista.getValueAt(row, 6);
            ocultar_coluna();

            try {
                String status = objeto.toString();
                if (status.equals("Ocupado")){
                    backgroud = Color.ORANGE;
                }else if (status.equals("Manutenção")){
                    backgroud = Color.RED;
                }else if(status.equals("Limpar")){
                    backgroud = Color.PINK;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

            label.setBackground(backgroud);

            return label;

        }

    }
