    private void ListarQuarto() {

        try {
            fstatus_quarto objquarto = new fstatus_quarto();
            DefaultTableModel modelo = (DefaultTableModel) TB_lista.getModel();

            modelo.setNumRows(0);
            ArrayList<vquartos> lista = objquarto.PesquisarQuartos();

            for (int num = 0; num < lista.size(); num++) {
                modelo.addRow(new Object[]{
                    lista.get(num).getIdquartos(),
                    lista.get(num).getNumero(),
                    lista.get(num).getAndar(),
                    lista.get(num).getDescricao(),
                    lista.get(num).getCaracteristicas(),
                    lista.get(num).getValordiaria(),
                    lista.get(num).getEstado(),
                    lista.get(num).getTipoquarto()

                });

            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Listar quartos: " + erro);
        }
    }

